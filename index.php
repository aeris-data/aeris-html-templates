            <div class="textwidget">
                <p>Les recherches dans le domaine atmosphérique concernent des spécialités comme la dynamique, la physique et la chimie atmosphérique. Elles incluent aussi des travaux plus orientés vers l’étude de l’évolution du climat. Pour effectuer ces recherches, la communauté scientifique utilise des modèles mais aussi des observations qu’elles soient obtenues au sol, au moyen de satellites ou de véhicules aéroportés.</p>
                <p>Un des enjeux de ce siècle est de rendre accessible ces données à une large communauté, que cela soit pour des activités de recherche ou des applications commerciales. Ces données d’observations, pour être utiles, doivent être étalonnées, validées, homogénéisées. Les données sont le plus souvent traitées dans les laboratoires, transmises et intégrées dans des bases des données internationales en plus de leur site de diffusion nationale. Cette organisation nécessite le rassemblement de chercheurs et de personnels techniques en nombre significatif, avec des métiers et des compétences variés et complémentaires. De plus, depuis une dizaine d’années, il a été créé des bases de données ou des « pôles » thématiques, l’un dédié aux données de chimie atmosphérique (Ether) et l’autre aux données sur les interactions aérosols-nuages-rayonnement (Icare), ainsi que le service Sedoo qui gère les données de campagnes au sein de l’Observatoire Midi-Pyrénées et le service Climserv au sein du projet ESPRI de l’IPSL.</p>
                <p>La création du « Pôle de données et services atmosphère » a pour objectif général de faciliter et de valoriser l’utilisation des données atmosphériques qu’elles soient issues de satellites, moyens sols, avions, ou ballons. Pour cela, le pôle génère des produits à partir des observations, mais également de nombreux services d’aide à l’utilisation des données, d’aide aux campagnes, ou d’interface avec les modèles. Il a pour objectif de renforcer le dispositif existant. Il est constitué autour de quatre centres ou services de données qui sont définis comme ayant les moyens de gestion collective des données. À cela s’ajoute des laboratoires, des réseaux de laboratoires ou des centres d’expertise, éléments indispensables d’un pôle de données pour les développements algorithmiques et les prototypages. Une évolution importante par rapport aux bases de données ou pôles existants consiste à se doter d’une gouvernance effective.</p>
