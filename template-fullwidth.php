            <p>The 3 MSEC experimental catchments have similar geological (sandstone, shale, schists) and pedological (Alfisol, Ultisol, Entisol) features. However, they are subjected to a dual gradient of both climate (from Thailand the driest to Vietnam the wettest) and agricultural intensity (from intensive agriculture in Thailand to tree plantations in Vietnam).</p>
            <p>To estimate the total water and soil loss going out of the watersheds, several devices are used in the different sub-catchments:</p>
            <ul>
                <li>Manual rain gauges and automatic weather stations for rainfall and evapotranspiration measurements (Campbell weather station)</li>
                <li>Water level recorders for flow measurement (OTT SE 200 + Campbell data logger) and water electric conductivity (Aquistar CT2X sensor)</li>
                <li>Automatic water and sediment samplers for suspended load quantification</li>
                <li>Sediment traps for soil losses quantification (bed load)</li>
                <li>Electrical resistivity tomography for bank and hillslopes groundwater and streamwater interactions analysis</li>
                <li>GIS tools (Q-gis, Grass, ArcGIS) to build annual land use maps based on satellite images (Sentinel) and field observations (once per year)</li>
            </ul>
